static esp_err_t ssd1306_cmd(i2c_master_dev_handle_t dev, uint8_t cmd)
{
    uint8_t buffer[2] = {0x00, cmd}; // control byte 0x00 = command
    return i2c_master_transmit(dev, buffer, sizeof(buffer), -1);
}

esp_err_t ssd1306_init(i2c_master_dev_handle_t dev)
{
    uint8_t init_cmds[] = {
        0xAE,       // Display OFF
        0x20, 0x00, // Memory mode: Horizontal
        0xB0,       // Page 0
        0xC8,       // COM scan direction remapped
        0x00,       // low column start
        0x10,       // high column start
        0x40,       // start line
        0x81, 0x7F, // contrast
        0xA1,       // segment remap
        0xA6,       // normal display
        0xA8, 0x3F, // multiplex ratio (1/64 duty)
        0xA4,       // display follows RAM
        0xD3, 0x00, // display offset
        0xD5, 0x80, // clock divide
        0xD9, 0xF1, // pre-charge
        0xDA, 0x12, // COM pins hardware conf
        0xDB, 0x40, // VCOM detect
        0x8D, 0x14, // charge pump enable
        0xAF        // Display ON
    };

    for (int i = 0; i < sizeof(init_cmds); i++) {
        esp_err_t ret = ssd1306_cmd(dev, init_cmds[i]);
        if (ret != ESP_OK) {
            return ret;
        }
    }

    return ESP_OK;
}

void ssd1306_clear(i2c_master_dev_handle_t dev)
{
    uint8_t buf[SSD1306_WIDTH + 1];
    buf[0] = 0x40; // control byte = data

    memset(&buf[1], 0x00, SSD1306_WIDTH);

    for (int page = 0; page < 8; page++) {
        ssd1306_cmd(dev, 0xB0 + page); // page address
        ssd1306_cmd(dev, 0x00);        // lower column
        ssd1306_cmd(dev, 0x10);        // higher column
        i2c_master_transmit(dev, buf, sizeof(buf), -1);
    }
}

void ssd1306_draw_text(i2c_master_dev_handle_t dev, int row, int col, const char *text)
{
    while (*text) {
        char c = *text;
        // Fix: Check proper range for complete ASCII table
        if (c < 32 || c > 127) c = 32; // fallback to space

        // set page (row)
        ssd1306_cmd(dev, 0xB0 + row);
        // set column address
        ssd1306_cmd(dev, 0x00 + (col & 0x0F));
        ssd1306_cmd(dev, 0x10 + ((col >> 4) & 0x0F));

        // build buffer: control byte + 5 font bytes + 1 space
        uint8_t buf[1 + 6];
        buf[0] = 0x40; // data
        memcpy(&buf[1], font5x8[c - 32], 5);
        buf[6] = 0x00; // space between characters

        i2c_master_transmit(dev, buf, sizeof(buf), -1);

        // move cursor right for next char (6 pixels wide)
        col += 6;
        text++;
    }
}